package basics_numerals;
public class SwitchStatement {
    public static void main(String[] args) {
        int choice = 2;
        switch(choice){
            case 1: System.out.println("In case 1");
            break;
            case 2: System.out.println("In case 2");
            break;
            case 3: System.out.println("In Case 3");
        }

    }
}

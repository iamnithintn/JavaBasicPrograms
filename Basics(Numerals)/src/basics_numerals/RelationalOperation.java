package basics_numerals;
public class RelationalOperation {                       //output in the form of boolean values(true or false)
    public static void main(String[] args) {
        int a = 35;
        int b = 32;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
    }
}
